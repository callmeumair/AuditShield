/**
 * Generate PDF report from data
 * Returns PDF buffer and SHA-256 hash for tamper-evidence
 */
export async function generatePDFReport(data: ReportData): Promise<{
    pdfBuffer: Buffer;
    hash: string;
}> {
    const html = generateReportHTML(data);

    // Launch headless browser
    const browser = await puppeteer.launch({
        headless: true,
        args: ['--no-sandbox', '--disable-setuid-sandbox'],
    });

    try {
        const page = await browser.newPage();
        await page.setContent(html, { waitUntil: 'networkidle0' });

        // Generate PDF
        const pdfBuffer = await page.pdf({
            format: 'A4',
            printBackground: true,
            margin: {
                top: '20mm',
                right: '15mm',
                bottom: '20mm',
                left: '15mm',
            },
        }) as Buffer;

        // Generate hash
        const hash = generateReportHash(pdfBuffer);

        return { pdfBuffer, hash };
    } finally {
        await browser.close();
    }
}
